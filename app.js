const campEnd = new Date('2026-07-18T10:00:00+02:00');
const campStart = new Date('2026-07-04T14:00:00+02:00');

const $ = (sel) => document.querySelector(sel);
const $$ = (sel) => [...document.querySelectorAll(sel)];

function navTo(id) {
  $$('.page').forEach(p => p.classList.remove('active'));
  $('#' + id).classList.add('active');
  $$('.nav button').forEach(b => b.classList.toggle('active', b.dataset.page === id));
  localStorage.setItem('vik-page', id);
  window.scrollTo({top: 0, behavior: 'smooth'});
}

function renderNav() {
  $$('.nav button').forEach(btn => btn.addEventListener('click', () => navTo(btn.dataset.page)));
  navTo(localStorage.getItem('vik-page') || 'dashboard');
}

function updateCountdown() {
  const now = new Date();
  const diff = campEnd - now;
  const el = $('#countdown');
  const sub = $('#countdownSub');
  if (diff <= 0) {
    el.textContent = 'Tábor skončil 🥹';
    sub.textContent = 'Teď už jen dospat deficit a třídit historky.';
    return;
  }
  const days = Math.ceil(diff / (1000*60*60*24));
  const fromStart = now - campStart;
  let campDay = 'ještě nezačal';
  if (fromStart >= 0 && now <= campEnd) {
    campDay = Math.min(15, Math.floor(fromStart / (1000*60*60*24)) + 1) + '. den tábora';
  }
  el.textContent = days + ' dní do konce';
  sub.textContent = 'Aktuálně: ' + campDay + ' · konec počítán na 18. 7. 2026.';
}

