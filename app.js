function renderGames() {
  const list = $('#gameList');
  const detail = $('#gameDetail');
  const filters = [...new Set(GAMES.map(g => g.cat))];
  $('#filters').innerHTML = '<button class="chip active" data-filter="all">Vše</button>' + filters.map(f => `<button class="chip" data-filter="${f}">${f}</button>`).join('');
  function drawList(filter='all') {
    list.innerHTML = GAMES.filter(g => filter === 'all' || g.cat === filter).map(g => `
      <button class="game-card" data-id="${g.id}">
        <b>${g.title}</b>
        <span>${g.cat} · ${g.time} · energie: ${g.energy}</span>
      </button>
    `).join('');
    $$('.game-card').forEach(card => card.addEventListener('click', () => showGame(card.dataset.id)));
    if (!detail.innerHTML && GAMES.length) showGame(GAMES[0].id);
  }
  function listItems(arr) { return '<ul>' + arr.map(i => `<li>${i}</li>`).join('') + '</ul>'; }
  function showGame(id) {
    const g = GAMES.find(x => x.id === id);
    $$('.game-card').forEach(c => c.classList.toggle('selected', c.dataset.id === id));
    detail.innerHTML = `
      <div class="detail-head">
        <div>
          <p class="eyebrow">${g.cat}</p>
          <h2>${g.title}</h2>
          <div class="badges">${badge(g.time)}${badge('energie: ' + g.energy)}${badge(g.people)}</div>
        </div>
        <button class="print-btn" onclick="window.print()">Tisk</button>
      </div>
      ${g.image ? `<img class="hero-img" src="assets/${g.image}" alt="${g.title}">` : ''}
      ${g.extra_images ? `<div class="thumb-grid">` + g.extra_images.map(img => `<img class="detail-thumb" src="assets/${img}" alt="${g.title} – doplňkový materiál">`).join('') + `</div>` : ''}
      <section><h3>Cíl hry</h3><p>${g.goal}</p></section>
      <section><h3>Příběhový úvod pro holky</h3><p class="story">${g.story}</p></section>
      <section><h3>Příprava pro vedoucí</h3>${listItems(g.prep)}</section>
      <section><h3>Pomůcky</h3>${listItems(g.materials)}</section>
      <section><h3>Pravidla krok za krokem</h3>${listItems(g.rules)}</section>
      <section><h3>Bodování</h3>${listItems(g.scoring)}</section>
      <section><h3>Poznámky pro vedoucí / méně stresu</h3>${listItems(g.leader)}</section>
      <section><h3>Materiály v aplikaci</h3>${listItems(g.printables)}</section>
    `;
  }
  $('#filters').addEventListener('click', e => {
    if (e.target.classList.contains('chip')) {
      $$('.chip').forEach(c => c.classList.remove('active'));
      e.target.classList.add('active');
      drawList(e.target.dataset.filter);
    }
  });
  $('#gameSearch').addEventListener('input', e => {
    const q = e.target.value.toLowerCase();
    $$('.game-card').forEach(card => {
      card.style.display = card.innerText.toLowerCase().includes(q) ? '' : 'none';
    });
  });
  drawList();
}

